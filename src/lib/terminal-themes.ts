const STORAGE_KEY = 'vibeshell-term-theme';

export function getStoredThemeId(): string {
  try {
    return localStorage.getItem(STORAGE_KEY) || 'github-dark';
  } catch {
    return 'github-dark';
  }
}

export function setStoredThemeId(id: string): void {
  localStorage.setItem(STORAGE_KEY, id);
}

export function getTerminalTheme(id: string): TerminalTheme {
  return terminalThemes.find((t) => t.id === id) || terminalThemes[0];
}
