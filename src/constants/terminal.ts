/** 终端 ANSI 颜色码 */

/** 黄色（警告/连接中） */
export const ANSI_YELLOW = '\x1b[33m';

/** 绿色（成功/已连接） */
export const ANSI_GREEN = '\x1b[32m';

/** 红色（错误/断开） */
export const ANSI_RED = '\x1b[31m';

/** 重置颜色 */
export const ANSI_RESET = '\x1b[0m';

/** 回车换行 */
export const ANSI_NEWLINE = '\r\n';

/** kill 命令前缀 */
export const KILL_COMMAND = 'kill -9';
